#OPERACIONES
# +	Addition	x + y	
# -	Subtraction	x - y	
# *	Multiplication	x * y	
# /	Division	x / y	
# %	Modulus	x % y	
# **	Exponentiation	x ** y	
# //	Floor division	x // y

# Operator	Example	     Same As
# =	x = 5	x = 5	
# +=	    x += 3	    x = x + 3	
# -=	    x -= 3	    x = x - 3	
# *=	    x *= 3	    x = x * 3	
# /=	    x /= 3	    x = x / 3	
# %=	    x %= 3	    x = x % 3	
# //=	    x //= 3	    x = x // 3	
# **=	    x **= 3	    x = x ** 3	
# &=	    x &= 3	    x = x & 3	
# |=	    x |= 3	    x = x | 3	
# ^=	    x ^= 3	    x = x ^ 3	
# >>=	    x >>= 3	    x = x >> 3	
# <<=	    x <<= 3	    x = x << 3	